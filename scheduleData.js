export function validateManualSchedule(schedule = MANUAL_SCHEDULE) {
  const pairCounts = new Map();
  const homeAwayCounts = new Map();
  const teamMatchCounts = new Map(LCK_TEAMS.map((team) => [team, 0]));
  const issues = [];

  for (const match of schedule) {
    const { teamA, teamB, id } = match;
    if (!LCK_TEAMS.includes(teamA) || !LCK_TEAMS.includes(teamB)) {
      issues.push(`[${id}] 알 수 없는 팀 코드: ${teamA} vs ${teamB}`);
      continue;
    }
    if (teamA === teamB) {
      issues.push(`[${id}] 자기 자신과의 경기`);
      continue;
    }

    teamMatchCounts.set(teamA, (teamMatchCounts.get(teamA) ?? 0) + 1);
    teamMatchCounts.set(teamB, (teamMatchCounts.get(teamB) ?? 0) + 1);

    const pairKey = [teamA, teamB].sort().join("__");
    pairCounts.set(pairKey, (pairCounts.get(pairKey) ?? 0) + 1);

    const homeAwayKey = `${teamA}__${teamB}`;
    homeAwayCounts.set(homeAwayKey, (homeAwayCounts.get(homeAwayKey) ?? 0) + 1);
  }

  for (const [team, count] of teamMatchCounts.entries()) {
    if (count !== 18) {
      issues.push(`${team}: 총 ${count}경기 (기대값 18)`);
    }
  }

  for (const [pair, count] of pairCounts.entries()) {
    if (count !== 2) {
      issues.push(`${pair.replace('__', ' vs ')}: ${count}회 (기대값 2)`);
      continue;
    }
    const [left, right] = pair.split("__");
    const leftHome = homeAwayCounts.get(`${left}__${right}`) ?? 0;
    const rightHome = homeAwayCounts.get(`${right}__${left}`) ?? 0;
    if (leftHome !== 1 || rightHome !== 1) {
      issues.push(`${left} vs ${right}: 홈/어웨이 분배 불일치 (${leftHome}:${rightHome})`);
    }
  }

  return {
    isValid: issues.length === 0,
    totalMatches: schedule.length,
    issues,
  };
}
